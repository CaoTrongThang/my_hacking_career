namespace mylib {
	// This is a simple function that returns the sum of two integers.
	int add(int a, int b) {
		return a + b;
	}
	// This is a simple function that returns the product of two integers.
	int multiply(int a, int b) {
		return a * b;
	}
	// This is a simple function that returns the difference of two integers.
	int subtract(int a, int b) {
		return a - b;
	}
	// This is a simple function that returns the quotient of two integers.
	double divide(int a, int b) {
		return static_cast<double>(a) / b;
	}
}